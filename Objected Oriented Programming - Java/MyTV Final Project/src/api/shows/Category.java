package api.shows;

/**
 * The Category enum represents the categories or genres of multimedia shows, such as TV shows or movies.
 * Each category provides a specific genre classification.
 *
 * @author Nikolaos Askitopoulos
 * @author Charalampos Deligiannakis
 */
public enum Category {

    /**
     * Represents the horror genre.
     */
    HORROR,

    /**
     * Represents the drama genre.
     */
    DRAMA,

    /**
     * Represents the science fiction (sci-fi) genre.
     */
    SCIFI,

    /**
     * Represents the comedy genre.
     */
    COMEDY,

    /**
     * Represents the action genre.
     */
    ACTION;
}